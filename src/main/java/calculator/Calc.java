package calculator;

public class Calc {
	
	public double add(int a, int b) {
		int result = 0;
		result = a + b;
		
		return result;
	}
	
	public double Subtract(int a, int b) {
		int result = 0;
		result = a - b;
		
		return result;
	}
	
	public int multiply(int a, int b) {
		int result = 0;
		result = a * b;
		
		return result;
	}
	
	public int divide(int a, int b) {
		int result = 0;
		result = a / b;
		
		return result;
	}
}
